    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        float preco = 0.00f;     
        try {
            preco = Float.parseFloat(tfPreco.getText());
        } catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(this, n.getMessage());
        }
        if (preco != 0) {
            Ingresso ingresso = new Ingresso(tftipo.getText(),
                    preco, tfNomeEvento.getText());
            IngressoDAO ingressoDao = new IngressoDAO();
            //JOptionPane.showConfirmDialog(this, "Deseja Gravar ?");
            ingressoDao.cadIngressos(ingresso);            
        }else{
            JOptionPane.showMessageDialog(this,"Valor Nulo ou Zerado");
        }


    }//GEN-LAST:event_jbSalvarActionPerformed

