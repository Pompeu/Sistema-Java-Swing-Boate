    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        jlMensagem.setText(null);
        tfNumeroCartao.setEditable(false);

        DefaultTableModel modelo = (DefaultTableModel) tblVenda.getModel();

        String quantidade = tfQunatidade.getText();
        try {
            quant = Integer.parseInt(quantidade);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

        valortotal = valorun * quant;

        if (!tfNumeroCartao.getText().trim().equals("") && !tfProduto.getText().trim().equals("")
                && quant > 0) {
            modelo.addRow(new Object[]{tfProduto.getText(), jlNomeProduto.getText(),
                quant, valorun, valortotal});
            CartaoConsumacaoControl cartaoControl = new CartaoConsumacaoControl();
            long prod_id = Long.parseLong(tfProduto.getText());

            int quantVenda = quant;

            cartaoControl.produtoMovimentacaoinserir(prod_id, venda_id, quantVenda);

        } else {
            JOptionPane.showMessageDialog(this, "Algum(s) campo(s) invalido(s)");
        }
        calctotal();

    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnAtulizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtulizarActionPerformed
        // TODO add your handling code here:
        jlMensagem.setText("");

        DefaultTableModel modelo = (DefaultTableModel) tblVenda.getModel();
        String quantidade = tfQunatidade.getText();
        try {
            quant = Integer.parseInt(quantidade);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        valortotal = valorun * quant;

        if (tblVenda.getSelectedRow() == -1) {
            if (tblVenda.getRowCount() == 0) {
                jlMensagem.setText("Tabela sem produtos");
            } else {
                jlMensagem.setText("Selecione um produto");
            }
        } else {
            modelo.setValueAt(tfProduto.getText(), tblVenda.getSelectedRow(), 0);
            modelo.setValueAt(jlNomeProduto.getText(), tblVenda.getSelectedRow(), 1);
            modelo.setValueAt(tfQunatidade.getText(), tblVenda.getSelectedRow(), 2);
            modelo.setValueAt(valorun, tblVenda.getSelectedRow(), 3);
            modelo.setValueAt(valortotal, tblVenda.getSelectedRow(), 4);
            CartaoConsumacaoControl cartaoControl = new CartaoConsumacaoControl();
            long produto_id = Long.parseLong(tfProduto.getText());
            int prod_quant = Integer.parseInt(tfQunatidade.getText());
            cartaoControl.produtoMovimentacaoAtulizar(produto_id, venda_id, prod_quant);
        }
        calctotal();
    }//GEN-LAST:event_btnAtulizarActionPerformed

