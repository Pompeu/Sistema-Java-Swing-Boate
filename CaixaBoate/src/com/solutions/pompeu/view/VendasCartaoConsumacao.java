    private void tfNumeroCartaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfNumeroCartaoActionPerformed
        // TODO add your handling code here:
        long numeroCartao = 0;
        try {
            numeroCartao = Long.parseLong(tfNumeroCartao.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        CartaoConsumacaoDAO CartaoDao = new CartaoConsumacaoDAO();
        String nome;
        nome = CartaoDao.listaCartao(numeroCartao).getNome();
        if (nome != null) {
            jlNomePropretario.setText(nome);
            CartaoConsumacaoDAO CartaoDao2 = new CartaoConsumacaoDAO();
            saldo = CartaoDao2.cartaoSAldo(numeroCartao);
            String saldoAtual = String.valueOf(nf.format(saldo));
            jlSaldo.setText(saldoAtual);
            jlMensagem.setText(null);

        } else {
            jlMensagem.setText("Numero invalido do cartao");
        }
        hablitarDesabilitacapus(false);

    }//GEN-LAST:event_tfNumeroCartaoActionPerformed

